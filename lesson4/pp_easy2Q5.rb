# There are a number of variables listed below. What are the different types and how do you know which is which?

excited_dog = "excited dog"
@excited_dog = "excited dog"
@@excited_dog = "excited dog"

=begin
excited dog is a local variable because it is all lowercase
@excited_dog is an instance variable because it has 1 @ in front of the variable name
@@excited_dog is a class variable because it has 2 @ in front of the variable name
=end

# What is the default return value of to_s when invoked on an object?
# Where could you go to find out if you want to be sure?

=begin
The default return value of to_s when invoked on an object is the object's class name and the
object id number

-You can call puts method on the object because when an object instantiated from a class
is passed in as an argument of the puts method invocation the to_s method is automatically called
=end
